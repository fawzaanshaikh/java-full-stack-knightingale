package knightingale.week2;

public class AsciiValues {
	public static void main(String[] args) {
		char character = 's';
		int Ascii = character;
		System.out.println("The ASCII value of " + character + " is: " + Ascii);
		
		int typeCastAscii = (int) character;
		System.out.println("The ASCII value of " + character + " is: " + typeCastAscii);
	}
}
